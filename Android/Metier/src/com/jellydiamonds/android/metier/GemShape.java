package com.jellydiamonds.android.metier;

public enum GemShape {
	
	Camer(1),
	Cushion(2),
	Fancy(3),
	Heart(4),
	Marquise(5),
	Octagon(6),
	Oval(7),
	Pear(8),
	Rectangle(9),
	Round(10),
	Ruff(11),
	Square(12),
	Trillion(13);
	
	protected int shape;
	
	GemShape( int shape )
	{
		this.shape = shape;
	}

	public Integer getValue()
	{
		return this.shape;
	}
}
